# Variables in ruby
# snake_case

person_age = "25"
person_name = "Chris"

puts ("Hello! " + person_name)
puts ("is it true that you are " + person_age + " years old!?")

# conditionals - if, elsif, else, end.

if (person_age > "25")
    puts "You're in your mid life!"
elsif (person_age < "25")
    puts "You're young!"
else
    puts "You must be 25!"
end